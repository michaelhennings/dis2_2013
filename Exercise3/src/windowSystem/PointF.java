package windowSystem;

public class PointF {

    private float x;
    private float y;

    public PointF(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public void setX(float value) {
        x = value;
    }

    public float getX() {
        return x;
    }

    public void setY(float value) {
        y = value;
    }

    public float getY() {
        return y;
    }
}
