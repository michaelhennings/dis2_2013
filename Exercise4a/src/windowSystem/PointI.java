package windowSystem;

public class PointI {

  public int x;

  public int y;

  public PointI(int x, int y) {
    this.x = x;
    this.y = y;
  }

  public void setX(int value) {
    x = value;
  }

  public int getX() {
    return x;
  }

  public void setY(int value) {
    y = value;
  }

  public int getY() {
    return y;
  }
}
